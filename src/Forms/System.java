    public void ShowData(){
        
        String[] titles={"Internal_ID","CC","Name","Country"};
        String[] registers=new String[4];
        
        DefaultTableModel model=new DefaultTableModel(null,titles);
        
        String SQL="select * from users";
        
        try{
            Statement st=connect.createStatement();
            ResultSet result= st.executeQuery(SQL);
            
            while(result.next()){
                registers[0]=result.getString("Internal_ID");
                registers[1]=result.getString("CC");
                registers[2]=result.getString("Name");
                registers[3]=result.getString("Country");
                
                model.addRow(registers);
            }
            
            TableUsers.setModel(model);
            
        }catch(Exception e){
            
            JOptionPane.showMessageDialog(null,"Error Showing Data" + e.getMessage());
        }
    }
    
    public void Delete(){
        int SelectedRow=TableUsers.getSelectedRow();
        
        try{
            String SQL="delete from users where Internal_ID="+TableUsers.getValueAt(SelectedRow, 0);
            
            Statement st=connect.createStatement();
            
            int n=st.executeUpdate(SQL);
            
            if (n>0){
                JOptionPane.showMessageDialog(null,"User Deleted");
            }
               
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Delete Error " + e.getMessage());
        }
    }
    
