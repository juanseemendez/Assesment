    public void Create(){
        try{
            String SQL="insert into users (CC,Name,Country) values (?,?,?)";

            PreparedStatement post=connect.prepareStatement(SQL);

            post.setString(1, TxCC.getText()); 
            post.setString(2, TxName.getText());
            post.setString(3, TxCountry.getText()); 

            post.execute();
            JOptionPane.showMessageDialog(null,"Successful Registration");

        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Registration Error " + e.getMessage());
        }

    } 
